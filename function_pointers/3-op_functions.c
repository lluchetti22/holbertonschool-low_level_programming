#include <stdio.h>
#include "3-calc.h"
#include <stdlib.h>

/**
 * op_add - do the code
 * @a: int
 * @b: int
 * Return: Nothing.
 */

int op_add(int a, int b)
{

return (a + b);

}

/**
 * op_sub - do the code
 * @a: int
 * @b: int
 * Return: Nothing.
 */

int op_sub(int a, int b)
{

return (a - b);

}

/**
 * op_mul - do the code
 * @a: int
 * @b: int
 * Return: Nothing.
 */

int op_mul(int a, int b)
{

return (a * b);

}

/**
 * op_div - do the code
 * @a: int
 * @b: int
 * Return: Nothing.
 */

int op_div(int a, int b)
{

return (a / b);

}

/**
 * int op_mod - do the code
 * @a: int
 * @b: int
 * Return: Nothing.
 */

int op_mod(int a, int b)
{

return (a % b);

}
